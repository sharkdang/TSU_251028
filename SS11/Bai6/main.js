








let a = parseInt(prompt("Nhập vào số a :"));

let b = parseInt(prompt("Nhập vào số b :"));

let x = prompt("Nhập vào các phép tính (+,-,/,*) :");

if (x==='+') {
    alert('Kết quả là :'+(a+b));
} else if (x==='-'){
        alert('Kết quả là :'+(a-b));
} else if (x==='/'){
    alert('Kết quả là :'+(a/b));
} else if (x==='*'){
        alert('Kết quả là :'+(a*b));
}